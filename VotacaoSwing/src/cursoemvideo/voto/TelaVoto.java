        int i = 2021- a;
        if(i < 16){
            lblR.setText("Nao vota");
        }else if((i>=16 && i<18) || (i>70)){
                lblR.setText("Voto Opcional.");
            }else{
                lblR.setText("Voto Obrigatorio");
            }
        
        
